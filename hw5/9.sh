#!/bin/bash

# Проверяем, передан ли аргумент
if [ -z "$1" ]; then
    echo "Ошибка: Укажите путь к файлу."
    echo "Использование: $0 <путь_к_файлу>"
    exit 1
fi

FILE=$1

# Обработка ошибок
if [ ! -e "$FILE" ]; then
    echo "Ошибка: Файл '$FILE' не существует."
elif [ ! -f "$FILE" ]; then
    echo "Ошибка: '$FILE' не является обычным файлом."
elif [ ! -r "$FILE" ]; then
    echo "Ошибка: Недостаточно прав для чтения файла '$FILE'."
else
    # Если всё хорошо, выводим содержимое
    echo "--- Содержимое файла: $FILE ---"
    cat "$FILE"
    echo "----------------------------"
fi
